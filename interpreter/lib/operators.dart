/// Represents the possible comparison operators used in filtering or ordering data.
///
/// Available operators:
/// - `eq`: Equal to
/// - `neq`: Not equal to
/// - `gt`: Greater than
/// - `lt`: Less than
/// - `gte`: Greater than or equal to
/// - `lte`: Less than or equal to
enum ComparisonOperator { eq, neq, gt, lt, gte, lte }

/// Represents the possible arithmetic operators used in calculations.
///
/// Available operators:
/// - `add`: Addition
/// - `sub`: Subtraction
/// - `mul`: Multiplication
/// - `div`: Division
enum ArithmeticOperator { add, sub, mul, div }
